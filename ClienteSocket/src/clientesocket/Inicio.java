    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        try {
            String ip = txtIp.getText();
            int puerto = Integer.parseInt(txtPuerto.getText());
            Socket conexion = new Socket(ip, puerto);
            PrintStream out = new PrintStream(conexion.getOutputStream());
            out.println(txtUsuario.getText());
            out.flush();
            new Conversacion(conexion, txtUsuario.getText()).setVisible(true);
            dispose();
        } catch (IOException | NumberFormatException | NoSuchAlgorithmException e) {
            e.printStackTrace(System.err);
        }

    }//GEN-LAST:event_btnIniciarActionPerformed

